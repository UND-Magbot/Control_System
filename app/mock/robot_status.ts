const TASK_TYPES = ["delivery", "monitoring", "patrol", "facility_inspection"] as const;

function randomInt(min: number, max: number) {
  return Math.floor(Math.random() * (max - min + 1)) + min;
}

function generateRandomTasks(minJobs = 7, maxJobs = 18) {
  const tasks = [];
  const count = randomInt(minJobs, maxJobs);

  for (let i = 0; i < count; i++) {
    tasks.push({
      taskName: `T${randomInt(100, 999)}`,
      taskType: TASK_TYPES[randomInt(0, TASK_TYPES.length - 1)],
      taskTime: randomInt(10, 180),
    });
  }

  return tasks;
}

function generateRandomErrors() {
  return [
    { errorType: "network", count: randomInt(0, 4) },
    { errorType: "fail", count: randomInt(0, 4) },
    { errorType: "mapping", count: randomInt(0, 4) },
    { errorType: "etc", count: randomInt(0, 4) },
  ];
}


